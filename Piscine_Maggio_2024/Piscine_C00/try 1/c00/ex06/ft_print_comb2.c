#include <unistd.h>

void	ft_print(int i, int j)
{
	char	c;

	c = (i / 10) + '0';
	write(1, &c, 1);
	c = (i % 10) + '0';
	write(1, &c, 1);
	write(1, " ", 1);
	c = (j / 10) + '0';
	write(1, &c, 1);
	c = (j % 10) + '0';
	write(1, &c, 1);
	if (! (i == 98 && j == 99))
	{
		write(1, ", ", 2);
	}
}

void	ft_print_comb2(void)
{
	int	i;
	int	j;

	i = 0;
	while (i <= 98)
	{
		j = i +1;
		while (j <= 99)
		{
			ft_print(i, j);
			j++;
		}
		i++;
	}
}
