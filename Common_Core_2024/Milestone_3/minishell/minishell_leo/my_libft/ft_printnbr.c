#include "libft.h"

int	ft_printnbr(int n)
{
	char	h;
	int		count;

	count = 0;
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
		count++;
	}
	if (n >= 10)
		count += ft_printnbr(n / 10);
	h = (n % 10) + '0';
	write (1, &h, 1);
	return (count + 1);
}
