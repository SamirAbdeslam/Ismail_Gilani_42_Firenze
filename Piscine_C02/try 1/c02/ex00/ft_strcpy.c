char	*ft_strcpy(char *dest, char *src)
{
	char	*copy_dest;

	copy_dest = dest;
	while (*src != '\0')
	{
		*dest = *src;
		dest++;
		src++;
	}
	*dest = '\0';
	return (copy_dest);
}
